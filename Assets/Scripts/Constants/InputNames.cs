/// <summary>
/// These are the names for the input via the Unity InputManager
/// </summary>
public static class InputNames
{
    /// <summary>
    /// Input name for horizontal input
    /// </summary>
    public const string Horizontal = "Horizontal";

    /// <summary>
    /// Input name for vertical input
    /// </summary>
    public const string Vertical = "Vertical";

    /// <summary>
    /// Input name for jumping input
    /// </summary>
    public const string Jump = "Jump";

    /// <summary>
    /// Input name for the primary attack
    /// </summary>
    public const string Attack = "Attack";

    /// <summary>
    /// Toggles between melee and ranged weapons
    /// </summary>
    public const string ToggleWeapon = "Toggle Weapon";

    /// <summary>
    /// Input name used to go back in a menu
    /// </summary>
    public const string Back = "Back";

    /// <summary>
    /// Input name used to switch tab to the left
    /// </summary>
    public const string TabLeft = "Tab to Left";

    /// <summary>
    /// Input name used to switch tab to the right
    /// </summary>
    public const string TabRight = "Tab to Right";

    /// <summary>
    /// Input name to edit the runes for the currently selected item
    /// </summary>
    public const string EditRunes = "Edit Runes";

    /// <summary>
    /// Input name to equip equipment
    /// </summary>
    public const string EquipEquipment = "Equip Equipment";

    /// <summary>
    /// Input name used to remove runes from equipment
    /// </summary>
    public const string ClearRunes = "Clear Runes";

    /// <summary>
    /// Input name to equip a rune
    /// </summary>
    public const string EquipRune = "Equip Rune";

    /// <summary>
    /// Input name for leveling up a rune
    /// </summary>
    public const string LevelUpRune = "Level Up Rune";

    /// <summary>
    /// Input name for buying a rune
    /// </summary>
    public const string BuyRune = "Buy Rune";

    /// <summary>
    /// Input name for interacting with an interactable
    /// </summary>
    public const string Interact = "Interact";

    /// <summary>
    /// Toggles the displayed quest to the right
    /// </summary>
    public const string QuestLeft = "Toggle Quest";
}